package com.gunjana.basics;

public class Basics {
    public static void main(String[] args) {
    // if condition
        int  a = 10;
        if(a == 10){
            System.out.println("Hello World");
        }
    // while condition : When you do not know how many times the loop is going to run
        int num = 1;
        while (num != 5){
            System.out.println(num);
            num++;
        }
    // for loop : When you know how many times the loop runs
        for (int no = 1; no != 5; no++){
            System.out.println(no);
        }
    }
}



